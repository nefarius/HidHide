using System;
using System.Collections.Generic;
using System.IO;
using System.IO.Compression;
using System.Linq;
using System.Net.Http;
using System.Threading.Tasks;

using Nuke.Common;
using Nuke.Common.CI.AppVeyor;
using Nuke.Common.IO;
using Nuke.Common.Tooling;
using Nuke.Common.Tools.MSBuild;
using Nuke.Common.ValueInjection;

using static Nuke.Common.IO.FileSystemTasks;
using static Nuke.Common.Tools.MSBuild.MSBuildTasks;

class Build : NukeBuild
{
    [Parameter("Configuration to build - Default is 'Debug' (local) or 'Release' (server)")]
    readonly string Configuration = IsLocalBuild ? "Debug" : "Release";

    [Parameter("Platform to build: x64 | ARM64. Default is current CI platform or x64 locally.")]
    readonly string Platform = IsLocalBuild ? "x64" : (AppVeyor.Instance.Platform ?? "x64");

    [Parameter("Optional path to PFX for CI test-signing MSI staging driver files. If omitted or missing, a throwaway code-signing PFX is generated (see AppVeyor env HIDHIDE_TEST_SIGN_PFX).")]
    readonly string? HidHideTestSignPfx;

    [Parameter("PFX password when using HIDHIDE_TEST_SIGN_PFX (required if that file exists; see HIDHIDE_TEST_SIGN_PFX_PASSWORD).")]
    [Secret]
    readonly string? HidHideTestSignPfxPassword;

    /// <summary>Resolves kit <c>signtool</c> via <see href="https://github.com/nefarius/wdkwhere">Nefarius.Tools.WDKWhere</see> (NUKE tool; same pattern as DsHidMini).</summary>
    [NuGetPackage("Nefarius.Tools.WDKWhere", "wdkwhere.dll", Framework = "net8.0")]
    readonly Tool WdkWhere;

    /// <summary>Password for autogenerated CI PFX only; not a security boundary (ephemeral cert, file deleted after signing).</summary>
    const string EphemeralTestPfxPassword = "HidHideCiEphemeral9#";

    /// <summary>
    /// Explicit repo-root solution path so CI does not depend on NUKE solution injection / .nuke parameters.
    /// </summary>
    static AbsolutePath SolutionFile => RootDirectory / "HidHide.sln";

    AbsolutePath ArtifactsDirectory => RootDirectory / "artifacts";
    AbsolutePath StagingRoot => ArtifactsDirectory / "staging";
    AbsolutePath OutputRoot => RootDirectory / "bin" / Configuration / Platform;

    AbsolutePath StageDir => StagingRoot / Platform;

    const string NefconVersion = "1.17.40";
    const string NefconZipName = $"nefcon_v{NefconVersion}.zip";
    static readonly string NefconZipUrl =
        $"https://github.com/nefarius/nefcon/releases/download/v{NefconVersion}/{NefconZipName}";

    Target Clean => _ => _
        .Before(Restore)
        .Executes(() =>
        {
            // AppVeyor runs `build.ps1` once per platform; clearing all of `artifacts/` would delete the other arch's MSI.
            if (IsLocalBuild)
                EnsureCleanDirectory(ArtifactsDirectory);
            else
                EnsureCleanDirectory(StagingRoot);
        });

    Target Restore => _ => _
        .Executes(() =>
        {
            MSBuild(s => s
                .SetTargetPath(SolutionFile)
                .SetTargets("Restore")
                .SetVerbosity(MSBuildVerbosity.Minimal));
        });

    Target Compile => _ => _
        .DependsOn(Restore)
        .Executes(() =>
        {
            var platform = ParsePlatform(Platform);

            MSBuild(s => s
                .SetTargetPath(SolutionFile)
                .SetTargets("Rebuild")
                .SetConfiguration(Configuration)
                .SetTargetPlatform(platform)
                .SetMaxCpuCount(Environment.ProcessorCount)
                .SetNodeReuse(IsLocalBuild)
                .SetVerbosity(MSBuildVerbosity.Minimal));
        });

    Target StageInstallerPayload => _ => _
        .DependsOn(Compile)
        .Executes(async () =>
        {
            EnsureCleanDirectory(StageDir);

            // Driver artifacts
            CopyFileToDirectory(OutputRoot / "HidHide" / "HidHide.sys", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHide" / "HidHide.inf", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHide" / "HidHide.cat", StageDir, FileExistsPolicy.Fail);

            // Kernel ETW
            CopyFileToDirectory(OutputRoot / "HidHide.man", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHide.wprp", StageDir, FileExistsPolicy.Fail);

            // User-mode binaries + ETW
            CopyFileToDirectory(OutputRoot / "HidHideClient.exe", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHideClient.man", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHideClient.wprp", StageDir, FileExistsPolicy.Fail);

            CopyFileToDirectory(OutputRoot / "HidHideCLI.exe", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHideCLI.man", StageDir, FileExistsPolicy.Fail);
            CopyFileToDirectory(OutputRoot / "HidHideCLI.wprp", StageDir, FileExistsPolicy.Fail);

            // Helper tool: nefcon windowless build (zip layout: x64/ / ARM64/ / x86/)
            await EnsureNefconwAsync(StageDir, Platform);
        });

    Target TestSignStagingDriver => _ => _
        .DependsOn(StageInstallerPayload)
        .Executes(() =>
        {
            if (IsLocalBuild)
                return;

            var sys = StageDir / "HidHide.sys";
            var cat = StageDir / "HidHide.cat";
            if (!File.Exists(sys) || !File.Exists(cat))
                throw new FileNotFoundException("Expected HidHide.sys and HidHide.cat in staging after StageInstallerPayload.");

            var userPfxPath = string.IsNullOrWhiteSpace(HidHideTestSignPfx) ? null : Path.GetFullPath(HidHideTestSignPfx);
            var userPfxExists = userPfxPath != null && File.Exists(userPfxPath);

            if (userPfxExists && string.IsNullOrWhiteSpace(HidHideTestSignPfxPassword))
            {
                throw new InvalidOperationException(
                    "When HIDHIDE_TEST_SIGN_PFX is set to an existing file, HIDHIDE_TEST_SIGN_PFX_PASSWORD must also be set.");
            }

            string pfxPath;
            string pfxPassword;
            var useTimestamp = false;
            string? ephemeralPfxToDelete = null;
            var scriptPath = (string?)null;

            if (userPfxExists)
            {
                pfxPath = userPfxPath!;
                pfxPassword = HidHideTestSignPfxPassword!;
                useTimestamp = true;
            }
            else
            {
                var signDir = (ArtifactsDirectory / ".ci-testsign").ToString();
                Directory.CreateDirectory(signDir);
                ephemeralPfxToDelete = Path.Combine(signDir, $"hidhide-{Platform}-{Guid.NewGuid():N}.pfx");
                scriptPath = Path.Combine(signDir, $"gen-{Guid.NewGuid():N}.ps1");
                WriteGenerateTestPfxScript(scriptPath);
                ProcessTasks.StartProcess(
                        "powershell.exe",
                        $"-NoProfile -NonInteractive -ExecutionPolicy Bypass -File \"{scriptPath}\" " +
                        $"-OutPath \"{ephemeralPfxToDelete}\" -PlainPassword \"{EphemeralTestPfxPassword}\"",
                        logInvocation: false,
                        logger: (_, s) => Logger.Normal(s))
                    .AssertZeroExitCode();

                if (!File.Exists(ephemeralPfxToDelete))
                    throw new InvalidOperationException("Ephemeral test PFX was not created.");

                pfxPath = ephemeralPfxToDelete;
                pfxPassword = EphemeralTestPfxPassword;
            }

            try
            {
                var inf2CatOs = Platform.Equals("ARM64", StringComparison.OrdinalIgnoreCase) ? "10_VB_ARM64" : "10_VB_X64";

                SignWithPfx(pfxPath, pfxPassword, sys, pageHash: true, timestamp: useTimestamp);

                var driverDir = StageDir.ToString().TrimEnd(Path.DirectorySeparatorChar, Path.AltDirectorySeparatorChar) +
                                Path.DirectorySeparatorChar;
                ProcessTasks.StartProcess(
                        "Inf2Cat.exe",
                        $"/driver:\"{driverDir}\" /os:{inf2CatOs} /verbose",
                        logInvocation: false,
                        logger: (_, s) => Logger.Normal(s))
                    .AssertZeroExitCode();

                SignWithPfx(pfxPath, pfxPassword, cat, pageHash: false, timestamp: useTimestamp);
            }
            finally
            {
                if (ephemeralPfxToDelete != null && File.Exists(ephemeralPfxToDelete))
                {
                    try { File.Delete(ephemeralPfxToDelete); }
                    catch { /* best-effort */ }
                }

                if (scriptPath != null && File.Exists(scriptPath))
                {
                    try { File.Delete(scriptPath); }
                    catch { /* best-effort */ }
                }
            }
        });

    Target BuildMsi => _ => _
        .DependsOn(TestSignStagingDriver)
        .Executes(() =>
        {
            // Run the WixSharp installer builder; on CI this runs on Windows.
            AbsolutePath installerProject = RootDirectory / "Installer" / "HidHide.Installer.csproj";
            AbsolutePath outDir = ArtifactsDirectory / "msi" / Platform;
            EnsureExistingDirectory(StageDir);
            EnsureExistingDirectory(outDir);

            var args =
                $"run --project \"{installerProject}\" -c {Configuration} -- " +
                $"--staging \"{StageDir}\" --out \"{outDir}\" --platform {Platform}";

            ProcessTasks.StartProcess("dotnet", args, RootDirectory)
                .AssertZeroExitCode();

            // Normalize output name so release scripts can rely on stable filenames.
            var builtMsi = Directory.GetFiles(outDir, "*.msi");
            if (builtMsi.Length != 1)
                throw new Exception($"Expected exactly one MSI in {outDir}, found {builtMsi.Length}.");

            var targetName = $"HidHide_{Platform}.msi";
            var targetPath = Path.Combine(outDir, targetName);
            if (File.Exists(targetPath))
                File.Delete(targetPath);
            File.Move(builtMsi[0], targetPath);
        });

    Target BuildCab => _ => _
        .DependsOn(Compile)
        .Executes(() =>
        {
            // Keep legacy DDF-driven attestation cab generation.
            string ddf = Platform.Equals("ARM64", StringComparison.OrdinalIgnoreCase)
                ? "HidHide_ARM64.ddf"
                : "HidHide_x64.ddf";

            ProcessTasks.StartProcess("makecab.exe", $"/f {ddf}", RootDirectory)
                .AssertZeroExitCode();
        });

    Target Ci => _ => _
        .DependsOn(BuildMsi)
        .DependsOn(BuildCab);

    public static int Main() => Execute<Build>(x => x.Ci);

    static MSBuildTargetPlatform ParsePlatform(string platform)
    {
        if (string.IsNullOrWhiteSpace(platform))
            throw new ArgumentNullException(nameof(platform));

        // Maps Build.Platform parameter to Nuke.Common.Tools.MSBuild.MSBuildTargetPlatform.
        return platform.ToUpperInvariant() switch
        {
            "ARM64" => (MSBuildTargetPlatform)"ARM64",
            "X64" => MSBuildTargetPlatform.x64,
            _ => throw new ArgumentException($"Unsupported platform: {platform}", nameof(platform))
        };
    }

    static async Task EnsureNefconwAsync(AbsolutePath stageDir, string platform)
    {
        var nefconw = stageDir / "nefconw.exe";
        if (File.Exists(nefconw))
            return;

        var temp = (AbsolutePath)Path.Combine(Path.GetTempPath(), "hidhide-nefcon");
        EnsureExistingDirectory(temp);

        var zipPath = temp / NefconZipName;
        if (!File.Exists(zipPath))
        {
            using var http = new HttpClient();
            using var resp = await http.GetAsync(NefconZipUrl);
            resp.EnsureSuccessStatusCode();
            await using var fs = File.OpenWrite(zipPath);
            await resp.Content.CopyToAsync(fs);
        }

        var extractDir = temp / $"nefcon-{NefconVersion}";
        if (Directory.Exists(extractDir))
            DeleteDirectory(extractDir);
        Directory.CreateDirectory(extractDir);
        ZipFile.ExtractToDirectory(zipPath, extractDir);

        string archDir = NefconZipArchFolder(platform);
        var src = Path.Combine(extractDir, archDir, "nefconw.exe");
        if (!File.Exists(src))
            throw new FileNotFoundException(
                $"nefconw.exe not found under '{archDir}' after extracting {zipPath}");

        CopyFileToDirectory(src, stageDir, FileExistsPolicy.Overwrite);
    }

    /// <summary>Subfolder inside nefcon release zip (see nefarius/nefcon GitHub releases).</summary>
    static string NefconZipArchFolder(string platform) =>
        platform.Equals("ARM64", StringComparison.OrdinalIgnoreCase) ? "ARM64" : "x64";

    void SignWithPfx(string pfxPath, string password, AbsolutePath file, bool pageHash, bool timestamp)
    {
        var args = new List<string> { "sign", "/v", "/fd", "SHA256", "/f", pfxPath, "/p", password };
        if (timestamp)
        {
            args.Add("/tr");
            args.Add("http://timestamp.digicert.com");
            args.Add("/td");
            args.Add("SHA256");
        }

        if (pageHash)
            args.Add("/ph");

        args.Add(file.ToString());
        RunWdkWhereSigntool(args.ToArray());
    }

    static void WriteGenerateTestPfxScript(string scriptPath)
    {
        const string body = """
            param(
                [Parameter(Mandatory = $true)][string]$OutPath,
                [Parameter(Mandatory = $true)][string]$PlainPassword
            )
            $ErrorActionPreference = 'Stop'
            $sec = ConvertTo-SecureString -String $PlainPassword -AsPlainText -Force
            $cert = New-SelfSignedCertificate -Type CodeSigningCert -Subject 'CN=HidHide CI test signing' `
                -CertStoreLocation 'Cert:\CurrentUser\My' -NotAfter (Get-Date).AddDays(42)
            try {
                Export-PfxCertificate -Cert $cert -FilePath $OutPath -Password $sec -Force | Out-Null
            }
            finally {
                $thumbPath = Join-Path 'Cert:\CurrentUser\My' $cert.Thumbprint
                if (Test-Path -LiteralPath $thumbPath) {
                    Remove-Item -LiteralPath $thumbPath -Force
                }
            }
            """;
        File.WriteAllText(scriptPath, body.Trim());
    }

    /// <summary>Runs <c>wdkwhere run signtool …</c> via the NUKE <see cref="WdkWhere"/> tool (<c>:nq</c> preserves argv for signtool).</summary>
    void RunWdkWhereSigntool(params string[] signtoolArguments)
    {
        if (signtoolArguments.Length == 0)
            throw new ArgumentException("At least one signtool argument is required.", nameof(signtoolArguments));

        var joined = string.Join(" ", signtoolArguments.Select(EscapeForCmdToken));
        var cmd = "run signtool " + joined;
        WdkWhere.Invoke($"{cmd:nq}");
    }

    static string EscapeForCmdToken(string value)
    {
        if (value.Length == 0)
            return "\"\"";

        if (value.Any(c => char.IsWhiteSpace(c) || c == '"'))
            return "\"" + value.Replace("\"", "\\\"") + "\"";

        return value;
    }
}

